CREATE DATABASE QLBH
USE QLBH

CREATE TABLE KHACHHANG (
    MaKH char(10),
    TenKH nvarchar(30) NOT NULL,
    GioiTinhKH nvarchar(5) NOT NULL CHECK (GioiTinhKH IN (N'Nam', N'Nữ')),
    SDTKH char(10) NOT NULL UNIQUE,
    NgaySinh date NOT NULL CHECK (NgaySinh <= GETDATE()),
    DiaChiKH nvarchar(100),
    DiemTichLuy int NOT NULL CHECK (DiemTichLuy >=0),
    HangThanhVien nvarchar(10) NOT NULL CHECK (HangThanhVien IN (N'Không', N'Bạc', N'Vàng')),
    MSTKH char(13),
	CONSTRAINT KHACHHANG_PK PRIMARY KEY (MaKH)
);

CREATE TABLE BOPHAN (
    MaBP char(10),
    TenBP nvarchar(20) NOT NULL,
    EmailBP nvarchar(50) NOT NULL UNIQUE,
    SDTBP char(10) NOT NULL UNIQUE,
	CONSTRAINT BOPHAN_PK PRIMARY KEY (MaBP)
);

CREATE TABLE CHUCVU (
    MaCV char(6),
    TenCV nvarchar(30) NOT NULL,
	CONSTRAINT CHUCVU_PK PRIMARY KEY (MaCV)
);

CREATE TABLE TAIKHOAN (
    MaTK char(6),
    MaNV char(10) UNIQUE, 
	TenDN nvarchar (30) UNIQUE NOT NULL,
    MatKhau nvarchar(30) NOT NULL,
	CONSTRAINT TAIKHOAN_PK PRIMARY KEY (MaTK)
);

CREATE TABLE LOAIQUYEN (
    MaLoaiQuyen char(6),
    TenLoaiQuyen nvarchar(30) NOT NULL UNIQUE,
    GhiChuLQ nvarchar(100),
	CONSTRAINT LOAIQUYEN_PK PRIMARY KEY (MaLoaiQuyen),
);


CREATE TABLE QUYEN (
    MaQuyen char(6),
	MaLoaiQuyen char (6),
    TenQuyen nvarchar(30) NOT NULL UNIQUE,
    MoTaQuyen nvarchar(100),
	CONSTRAINT QUYEN_PK PRIMARY KEY (MaQuyen),
	CONSTRAINT FK_QUYEN_LOAIQ FOREIGN KEY (MaLoaiQuyen) REFERENCES LOAIQUYEN (MaLoaiQUyen)
);

CREATE TABLE NHANVIEN (
    MaNV char(10),
    TenNV nvarchar(30) NOT NULL,
    MaBP char(10),
    MaCV char(6),
    MaTK char(6) UNIQUE,
    GioiTinhNV nvarchar(5) NOT NULL CHECK (GioiTinhNV IN (N'Nam', N'Nữ')) ,
    SDTNV char(10) NOT NULL UNIQUE,
    EmailNV nvarchar(30) NOT NULL,
    CONSTRAINT NHANVIEN_PK PRIMARY KEY (MaNV),
    CONSTRAINT FK_NV_BP FOREIGN KEY (MaBP) REFERENCES BOPHAN(MaBP),
    CONSTRAINT FK_NV_CV FOREIGN KEY (MaCV) REFERENCES CHUCVU(MaCV),
    CONSTRAINT FK_NV_TK FOREIGN KEY (MaTK) REFERENCES TAIKHOAN(MaTK)
);

CREATE TABLE LOAISANPHAM (
    MaLoaiSP char(6),
    TenLoaiSP nvarchar(30) NOT NULL UNIQUE,
	CONSTRAINT LOAISANPHAM_PK PRIMARY KEY (MaLoaiSP)
);

CREATE TABLE DVT (
    MaDVT char(6),
    TenDVT nvarchar(30) NOT NULL UNIQUE,
	CONSTRAINT DVT_FK PRIMARY KEY (MaDVT)
);

CREATE TABLE KHO (
    MaKho char(6),
    DiaChiKho nvarchar(100) NOT NULL,
    SDTKho char(10) NOT NULL UNIQUE,
	CONSTRAINT KHO_FK PRIMARY KEY (MaKho)
);

CREATE TABLE CTKM (
    MaCTKM char(10),
    TenCTKM nvarchar(30) NOT NULL UNIQUE, 
    MoTaCTKM nvarchar(100),
    NgayBatDau datetime NOT NULL,
    NgayKetThuc datetime NOT NULL,
    CHECK (NgayKetThuc >= NgayBatDau),
	CONSTRAINT CTKM_FK PRIMARY KEY (MaCTKM)
);

CREATE TABLE SANPHAM (
    MaSP char(10),
    MaKho char(6),
    MaLoaiSP char(6),
    MaDVT char(6),
    MaCTKM char(10),
	GiaBan DECIMAL(18,2),
	Barcode char(13) UNIQUE,
	TenSP nvarchar(30) NOT NULL,
    NSXSP date NOT NULL,
    HSDSP date NOT NULL,
	CHECK (HSDSP >= NSXSP),
	XuatXu nvarchar(50),
	NhaCungCap nvarchar(50),
	VAT DECIMAL(18,2) CHECK (VAT >= 0),
	TrongLuong DECIMAL(18,2),
	SoLuongToiThieu int DEFAULT 0,
    HinhAnhSP image,
    MoTaSP nvarchar(100),
	CONSTRAINT SANPHAM_PK PRIMARY KEY (MaSP), 
    CONSTRAINT FK_SP_KHO FOREIGN KEY (MaKho) REFERENCES KHO(MaKho),
    CONSTRAINT FK_SP_LOAISP FOREIGN KEY (MaLoaiSP) REFERENCES LOAISANPHAM(MaLoaiSP),
    CONSTRAINT FK_SP_DVT FOREIGN KEY (MaDVT) REFERENCES DVT(MaDVT),
    CONSTRAINT FK_SP_CTKM FOREIGN KEY (MaCTKM) REFERENCES CTKM(MaCTKM)
);

CREATE TABLE HOADON (
    SoHD char(10) ,
    MaNV char(10),
    MaKH char(10),
    NgayLap datetime NOT NULL,
    PhuongThucTT nvarchar(20) NOT NULL,
    TrangThaiHD nvarchar(30) NOT NULL,
    TriGiaHD DECIMAL(18,2) NOT NULL,
	GiamGia DECIMAL(18,2) DEFAULT 0 CHECK (GiamGia >= 0),
    CHECK (TriGiaHD >= 0),
    CHECK (PhuongThucTT IN (N'Tiền mặt', N'Chuyển khoản', N'Thẻ')),
	CONSTRAINT HOADON_PK PRIMARY KEY (SoHD), 
    CONSTRAINT FK_HD_NV FOREIGN KEY (MaNV) REFERENCES NHANVIEN(MaNV),
    CONSTRAINT FK_HD_KH FOREIGN KEY (MaKH) REFERENCES KHACHHANG(MaKH)
);

CREATE TABLE CT_HOADON (
    SoHD char(10) NOT NULL,
    MaSP char(10) NOT NULL,
    SoLuongHD smallint NOT NULL CHECK (SoLuongHD > 0),
    DonGiaHD DECIMAL(18,2) NOT NULL CHECK (DonGiaHD >= 0),
    VATHD DECIMAL(18,2) NOT NULL CHECK (VATHD >= 0),
    ThanhTienHD AS (SoLuongHD * DonGiaHD * (1 + VATHD)) PERSISTED,
	CONSTRAINT CTHD_PK PRIMARY KEY (SoHD, MaSP),
	CONSTRAINT FK_CTHD_HD FOREIGN KEY (SoHD) REFERENCES HOADON (SoHD),
	CONSTRAINT FK_CTHD_SP FOREIGN KEY (MaSP) REFERENCES SANPHAM (MaSP)
);

CREATE TABLE TONKHO (
    MaSP char(10) NOT NULL,
    MaKho char(6) NOT NULL,
    NgayCapNhat datetime NOT NULL,
    ThangTK smallint NOT NULL CHECK (ThangTK BETWEEN 1 AND 12),
    NamTK smallint NOT NULL CHECK (NamTK > 2000), 
    TonDauKy smallint NOT NULL CHECK (TonDauKy >= 0),
    TriGiaTDK DECIMAL(18,2) NOT NULL CHECK (TriGiaTDK >= 0),
    NhapTK smallint NOT NULL CHECK (NhapTK >= 0),
    TriGiaNTK DECIMAL(18,2) NOT NULL CHECK (TriGiaNTK >= 0),
    XuatTK smallint NOT NULL CHECK (XuatTK >= 0),
    TriGiaXTK DECIMAL(18,2) NOT NULL CHECK (TriGiaXTK >= 0),
    TonCK smallint NOT NULL CHECK (TonCK >= 0),
    TriGiaTCK DECIMAL(18,2) NOT NULL CHECK (TriGiaTCK >= 0),
    CONSTRAINT TonKho_PK PRIMARY KEY (MaSP, MaKho),
    CONSTRAINT FK_TonKho_SP FOREIGN KEY (MaSP) REFERENCES SANPHAM(MaSP),
    CONSTRAINT FK_TonKho_Kho FOREIGN KEY (MaKho) REFERENCES KHO(MaKho),
    CHECK (TonCK = TonDauKy + NhapTK - XuatTK),
    CHECK (TriGiaTCK = TriGiaTDK + TriGiaNTK - TriGiaXTK)
);

CREATE TABLE PHANQUYEN
(
	MaTK char (6),
	MaQuyen char (6),
	CONSTRAINT PhanQuyen_PK PRIMARY KEY (MaQuyen, MaTK),
    CONSTRAINT FK_PhanQuyen_TK FOREIGN KEY (MaTK) REFERENCES TAIKHOAN(MaTK),
    CONSTRAINT FK_PhanQuyen_Quyen FOREIGN KEY (MaQuyen) REFERENCES QUYEN(MaQuyen)
);

